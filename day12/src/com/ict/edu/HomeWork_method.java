package com.ict.edu;

public class HomeWork_method {
	
	private String name ="";
	private int price =500;
	
	public HomeWork_method(String name, int price) {
		this.name = name;
		this.price = price;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
	
}
